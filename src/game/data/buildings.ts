function createFactionBuildings(faction: Faction): Partial<Record<BuildingType, BuildingData>> {
  const result: Partial<Record<BuildingType, BuildingData>> = {};
  const config = FACTION_BUILDING_CONFIGS[faction];
  const overrides = config?.overrides || {};
  const exclude = new Set(config?.exclude || []);

  for (const [type, baseData] of Object.entries(BASE_BUILDINGS)) {
    if (exclude.has(type as BuildingType)) continue;
    const override = overrides[type as BuildingType];
    result[type as BuildingType] = {
      ...baseData,
      faction,
      ...(override || {}),
    };
  }

  return result;
}

