function createFactionUnits(faction: Faction): Partial<Record<UnitType, UnitData>> {
  const result: Partial<Record<UnitType, UnitData>> = {};
  const config = FACTION_UNIT_CONFIGS[faction];
  const overrides = config.overrides || {};

  for (const unitType of config.include) {
    const baseData = BASE_UNITS[unitType];
    if (!baseData) continue;
    const override = overrides[unitType];
    result[unitType] = {
      ...baseData,
      faction,
      ...(override || {}),
    };
  }

  return result;
}

